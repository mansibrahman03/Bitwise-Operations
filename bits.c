/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 */
int bitXor(int x, int y) {
  return ~(x & y) & ~(~x & ~y);
}
/* 
 * isNotEqual - return 0 if x == y, and 1 otherwise 
 *   Examples: isNotEqual(5,5) = 0, isNotEqual(4,5) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int isNotEqual(int x, int y) {
  int result = !(x ^ y);
  return !(result);
}
/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
  int mask = 0xFF;
  int shift = n << 3;
  int shift2 = (x >> shift) & mask;
  return shift2;
}
/* 
 * copyLSB - set all bits of result to least significant bit of x
 *   Example: copyLSB(5) = 0xFFFFFFFF, copyLSB(6) = 0x00000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int copyLSB(int x) {
  int shift1 = x << 31;
  int shift2 = shift1 >> 31;
  return shift2;
}
/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 0 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3 
 */
int logicalShift(int x, int n) {
  int mask = 0x8;
  int shifted_mask = mask << 28;
  int shifted_mask2 = ~((shifted_mask >> n) << 1);
  int result = (x >> n) & shifted_mask2;
  return result;
}
/*
 * bitCount - returns count of number of 1's in word
 *   Examples: bitCount(5) = 2, bitCount(7) = 3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 40
 *   Rating: 4
 */
int bitCount(int x) {
  int mask = 0x11;
  int shifted_mask = (mask << 8) + mask;
  int shifted_mask2 = (shifted_mask << 8) + mask;
  int shifted_mask3 = (shifted_mask2 << 8) + mask;
  int a = x & shifted_mask3;
  int b = (x >> 1) & shifted_mask3;
  int c = (x >> 2) & shifted_mask3;
  int d = (x >> 3) & shifted_mask3;
  int sum = a+b+c+d;
  int new_mask = 0x0F;
  int mask1 = sum & new_mask;
  int mask2 = (sum >> 4) & new_mask;
  int mask3 = (sum >> 8) & new_mask;
  int mask4 = (sum >> 12) & new_mask;
  int mask5 = (sum >> 16) & new_mask;
  int mask6 = (sum >> 20) & new_mask;
  int mask7 = (sum >> 24) & new_mask;
  int mask8 = (sum >> 28) & new_mask;
  int result = mask1 + mask2 + mask3 + mask4 + mask5 + mask6 + mask7 + mask8;
  return result;
}
/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) {
  int twoscomplement = ~x + 1;
  int leastsignificantbit = twoscomplement & x;
  int x_shifted = (~leastsignificantbit + 1) >> 31;
  int mask = 0x01;
  int result = (~x_shifted & mask);
  return result;
}
/* 
 * leastBitPos - return a mask that marks the position of the
 *               least significant 1 bit. If x == 0, return 0
 *   Example: leastBitPos(96) = 0x20
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2 
 */
int leastBitPos(int x) {
  int twoscomplement = ~x + 1;
  int leastsignificantbit = twoscomplement & x;
  return leastsignificantbit;
}
/* 
 * TMax - return maximum two's complement integer 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 4
 *   Rating: 1
 */
int tmax(void) {
  int mask = 0x80;
  int shifted_mask = ~ (mask << 24);
  return shifted_mask;
}
/* 
 * isNonNegative - return 1 if x >= 0, return 0 otherwise 
 *   Example: isNonNegative(-1) = 0.  isNonNegative(0) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 3
 */
int isNonNegative(int x) {
  int mask = 0x80;
  int shifted_mask = mask << 24;
  x = !(x & shifted_mask);
  return x;
}
/* 
 * isGreater - if x > y  then return 1, else return 0 
 *   Example: isGreater(4, 5) = 0, isGreater(5, 4) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isGreater(int x, int y) {
  int mask = 0x80;
  int shifted_mask = mask << 24;
  int checksignof_x = !(x & shifted_mask);
  int checksignof_y = !(y & shifted_mask);
  int bothsamesign = !(checksignof_x ^ checksignof_y);
  int x_positive = checksignof_x & (!(checksignof_y));
  int x_negative = (!(checksignof_x)) & checksignof_y;
  int sum = !(((~y + 1) + (x + ~0)) & shifted_mask);
  return (((sum & bothsamesign) | x_positive) & !x_negative);
}
/* 
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int divpwr2(int x, int n) {
  int mask = x >> 31;
  int result = x + (((1 << n) + ~0) & mask);
  return result >> n;
}
/* 
 * absVal - absolute value of x
 *   Example: absVal(-1) = 1.
 *   You may assume -TMax <= x <= TMax
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int absVal(int x) {
  int mask = x >> 31;
  int result = mask ^ x;
  return result + (mask & 1);
}
/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000, 0x80000000) = 0,
 *            addOK(0x80000000, 0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
  int mask = 0x80;
  int shifted_mask = mask << 24;
  int checksignof_x = !(x & shifted_mask);
  int checksignof_y = !(y & shifted_mask);
  int bothdifferentsign = (checksignof_x ^ checksignof_y);
  int bothpositive = checksignof_x & checksignof_y;
  int bothnegative = (!checksignof_x) & (!checksignof_y);
  int sum = x + y;
  int checksignof_sum = !(sum & shifted_mask);
  return (((checksignof_sum & bothpositive) | ((!checksignof_sum) & bothnegative)) | bothdifferentsign);
}
